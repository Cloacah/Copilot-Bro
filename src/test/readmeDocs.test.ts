test("README is generated from the checked-in multilingual config", () => {
	assert.match(
		README,
		/This README is generated by scripts\/readme\/generate-readme\.mjs from docs\/readme\.config\.json and docs\/readme\.sections\.json/
	);
	assert.deepEqual(README_CONFIG.languages?.map((language) => language.id), ["zh", "en"]);
	execFileSync(process.execPath, ["scripts/readme/readme-section-parity.mjs"], {
		cwd: resolve(__dirname, "../.."),
		stdio: "ignore"
	});
	execFileSync(process.execPath, ["scripts/readme/generate-readme.mjs", "--check"], {
		cwd: resolve(__dirname, "../.."),
		stdio: "ignore"
	});
});

test("readme.sections.json keeps zh/en parity for every sectionOrder entry", () => {
	const generated = new Set(README_CONFIG.generatedSections ?? []);
	for (const id of README_CONFIG.sectionOrder ?? []) {
		if (generated.has(id)) {
			continue;
		}
		const block = README_SECTIONS[id];
		assert.ok(block, `missing section ${id}`);
		assert.ok(String(block.zh ?? "").trim().length > 0, `empty zh for ${id}`);
		assert.ok(String(block.en ?? "").trim().length > 0, `empty en for ${id}`);
		assert.ok(block.title?.zh, `missing title.zh for ${id}`);
		assert.ok(block.title?.en, `missing title.en for ${id}`);
	}
});

