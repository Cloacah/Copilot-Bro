test("README compatibility matrix rows stay aligned with runtime matrix entries", () => {
	const modelTypes: CompatibilityMatrixInput["modelType"][] = ["builtin", "bro"];
	const visionCapabilities: CompatibilityMatrixInput["visionCapability"][] = ["vision", "non-vision"];
	const toolsAvailability: CompatibilityMatrixInput["toolsAvailable"][] = ["tools-available", "no-tools"];
	const agentFlags: boolean[] = [true, false];

	for (const modelType of modelTypes) {
		for (const visionCapability of visionCapabilities) {
			for (const toolsAvailable of toolsAvailability) {
				for (const agentEnabled of agentFlags) {
					const input: CompatibilityMatrixInput = {
						modelType,
						visionCapability,
						toolsAvailable,
						agentEnabled
					};
					const entry = getCompatibilityMatrixEntry(input);
					const row = `| ${modelType} | ${visionCapability} | ${toReadmeToolsLabel(toolsAvailable)} | ${agentEnabled ? "on" : "off"} | ${entry.strategy} | ${entry.fallbackStrategy ?? "-"} |`;
					assert.match(README, new RegExp(escapeRegExp(row)));
				}
			}
		}
	}
});

function toReadmeToolsLabel(value: CompatibilityMatrixInput["toolsAvailable"]): "tools" | "no-tools" {
	return value === "tools-available" ? "tools" : "no-tools";
}

function escapeRegExp(value: string): string {
	return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}